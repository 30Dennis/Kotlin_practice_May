fun main(args: Array<String>) {
    println("Number Range")
    for (num in 3..9){
        println(num)
    }

    println("Number Range")
    for (num in 10..15)
        println(num)

    println("Character Range")
    for (chars in 'c'..'i')
        println(chars)

    println("Character Range")
    for (char in 'f'..'j')
        println(char)




}